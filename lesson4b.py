# for loop- used in conditions- used in repeat block of codes using for 
# 1. loop from 1 to 10 
for count in range(1, 10, 1,):
    print(count)

# 2. loop from 100 , 200 
for count in range(100, 200, 1):
    print(count)    

# 3. loop from 50, -50 
for count in range(-50, 50, 1):
    print(count)    

# 4. loop even numbers from 100, 200 
for count in range(100, 200, 2):
    print(count)    